import fs from 'fs';
import path from 'path';
import * as simpleIcons from 'simple-icons';

const projectRoot = process.cwd();
const sourcePath = path.join(projectRoot, 'src', 'app', 'static-brand-icons.json');
const outputDirectoryPath = path.join(projectRoot, 'src', 'app', 'generated');
const outputPath = path.join(outputDirectoryPath, 'static-brand-icons.ts');

function normalizeBrandIconKey(value) {
  return value
    .trim()
    .replace(/^icon:/i, '')
    .toLowerCase()
    .replace(/[^a-z0-9]/g, '');
}

function buildIconAliasSet(icon) {
  return new Set([
    icon.slug,
    icon.title,
    icon.slug.replace(/-/g, ''),
    icon.title.replace(/[^a-zA-Z0-9]/g, ''),
  ]);
}

function loadRequestedIcons() {
  const raw = fs.readFileSync(sourcePath, 'utf8');
  const parsed = JSON.parse(raw);

  if (!Array.isArray(parsed)) {
    throw new Error(`${path.relative(projectRoot, sourcePath)} must export a JSON array`);
  }

  return parsed.map((value, index) => {
    if (typeof value !== 'string' || !value.trim()) {
      throw new Error(`Invalid icon name at index ${index} in ${path.relative(projectRoot, sourcePath)}`);
    }

    return value.trim();
  });
}

function buildSimpleIconMap() {
  const iconMap = new Map();
  const moduleEntries = simpleIcons;

  Object.keys(moduleEntries)
    .filter((key) => key.startsWith('si') && key.length > 2)
    .forEach((key) => {
      const icon = moduleEntries[key];

      buildIconAliasSet(icon).forEach((alias) => {
        const normalizedAlias = normalizeBrandIconKey(alias);

        if (!normalizedAlias || iconMap.has(normalizedAlias)) {
          return;
        }

        iconMap.set(normalizedAlias, icon);
      });
    });

  return iconMap;
}

function resolveRequestedIcons(requestedIcons, iconMap) {
  const resolvedIcons = [];
  const seenSlugs = new Set();

  requestedIcons.forEach((name) => {
    const normalizedName = normalizeBrandIconKey(name);
    const icon = iconMap.get(normalizedName);

    if (!icon) {
      throw new Error(`Unknown static brand icon "${name}" in ${path.relative(projectRoot, sourcePath)}`);
    }

    if (seenSlugs.has(icon.slug)) {
      return;
    }

    seenSlugs.add(icon.slug);
    resolvedIcons.push(icon);
  });

  return resolvedIcons.sort((left, right) => left.slug.localeCompare(right.slug));
}

function renderOutput(resolvedIcons) {
  const lines = [
    '// This file is auto-generated by scripts/generate-static-brand-icons.mjs',
    `// Source: ${path.relative(projectRoot, sourcePath)}`,
    '',
    "import type { ResolvedIconData } from '../icon-types'",
    '',
    'export const STATIC_BRAND_ICONS = {',
  ];

  resolvedIcons.forEach((icon) => {
    lines.push(
      `  ${JSON.stringify(icon.slug)}: { kind: 'brand', path: ${JSON.stringify(icon.path)} },`,
    );
  });

  lines.push(
    '} satisfies Record<string, ResolvedIconData>',
    '',
  );

  return `${lines.join('\n')}`;
}

function main() {
  const requestedIcons = loadRequestedIcons();
  const iconMap = buildSimpleIconMap();
  const resolvedIcons = resolveRequestedIcons(requestedIcons, iconMap);
  const output = renderOutput(resolvedIcons);

  fs.mkdirSync(outputDirectoryPath, { recursive: true });
  fs.writeFileSync(outputPath, output);

  console.log(
    `Generated ${path.relative(projectRoot, outputPath)} with ${resolvedIcons.length} static brand icon${resolvedIcons.length === 1 ? '' : 's'}`,
  );
}

main();
